"""7 - Faça um algoritmo que leia dois valores booleanos (lógicos) 
e determine se ambos são VERDADEIRO ou FALSO."""
print("Digite dois valores booleanos: ")
valor1 = bool(input())
valor2 = bool(input())

if valor1 == True and valor2 == True:
    print("Ambos são verdadeiros")

elif valor1 == False and valor2 == False:

    print("Ambos são falsos")


